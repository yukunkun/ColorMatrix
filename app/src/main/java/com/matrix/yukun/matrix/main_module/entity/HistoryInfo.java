public class HistoryInfo {

    /**
     * year : 2002
     * month : 1
     * day : 1
     * title : 欧元正式进入流通
     * type : 1
     */

    private String year;
    private int month;
    private int day;
    private String title;
    private int type;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }


}
